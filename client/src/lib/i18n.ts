export type Language = 'ar' | 'en';
export type TranslationKey = keyof typeof translations.ar;

export const getTranslation = (key: TranslationKey, lang: Language = 'ar'): string => {
    const primaryLang = lang === 'ar' ? 'ar' : 'en';
    const fallbackLang = lang === 'ar' ? 'en' : 'ar';

    return (translations[primaryLang] as any)[key] || (translations[fallbackLang] as any)[key] || key;
};

export const getCurrentLanguage = (): Language => {
    if (typeof window === 'undefined') return 'ar';
    return (localStorage.getItem('language') as Language) || 'ar';
};

export const setLanguage = (lang: Language): void => {
    if (typeof window === 'undefined') return;
    localStorage.setItem('language', lang);
    document.cookie = `language=${lang}; path=/; max-age=31536000; SameSite=Lax`;
    window.location.reload();
};
